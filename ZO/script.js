  function initSnakeGame() {
    const canvas = document.getElementById('snakeCanvas');
    const ctx = canvas.getContext('2d');
    const scoreEl = document.getElementById('score');
    const highScoreEl = document.getElementById('highScore');
    
    const gridSize = 15;
    const gridWidth = Math.floor(canvas.width / gridSize);
    const gridHeight = Math.floor(canvas.height / gridSize);
    
    let snake = [];
    let food = {};
    let direction = 'right';
    let nextDirection = 'right';
    let score = 0;
    let gameInterval = null;
    let isRunning = false;
    
    let highScore = parseInt(localStorage.getItem('snakeHighScore') || '0');
    highScoreEl.textContent = highScore;
  
    function resetGame() {
      snake = [
        {x: 5, y: 10},
        {x: 4, y: 10},
        {x: 3, y: 10}
      ];
      generateFood();
      direction = 'right';
      nextDirection = 'right';
      score = 0;
      scoreEl.textContent = score;
      draw();
    }
  
    function generateFood() {
      food = {
        x: Math.floor(Math.random() * gridWidth),
        y: Math.floor(Math.random() * gridHeight)
      };
      for (let segment of snake) {
        if (segment.x === food.x && segment.y === food.y) {
          return generateFood();
        }
      }
    }
  
    function draw() {
      ctx.fillStyle = '#0a0a0a';
      ctx.fillRect(0, 0, canvas.width, canvas.height);
      
      ctx.fillStyle = '#4fc3f7';
      for (let segment of snake) {
        ctx.fillRect(segment.x * gridSize, segment.y * gridSize, gridSize - 1, gridSize - 1);
      }
      
      ctx.fillStyle = '#FF6F00';
      ctx.fillRect(food.x * gridSize, food.y * gridSize, gridSize - 1, gridSize - 1);
    }
  
    function update() {
      direction = nextDirection;
      
      const head = {...snake[0]};
      switch (direction) {
        case 'up': head.y--; break;
        case 'down': head.y++; break;
        case 'left': head.x--; break;
        case 'right': head.x++; break;
      }
      
      // –°—Ç–æ–ª–∫–Ω–æ–≤–µ–Ω–∏–µ —Å–æ —Å—Ç–µ–Ω–æ–π
      if (head.x < 0 || head.x >= gridWidth || head.y < 0 || head.y >= gridHeight) {
        gameOver();
        return;
      }
      
      // –°—Ç–æ–ª–∫–Ω–æ–≤–µ–Ω–∏–µ —Å —Å–æ–±–æ–π
      for (let i = 0; i < snake.length; i++) {
        if (snake[i].x === head.x && snake[i].y === head.y) {
          gameOver();
          return;
        }
      }
      
      snake.unshift(head);
      
      // –°—ä–µ–ª–∞ –µ–¥—É?
      if (head.x === food.x && head.y === food.y) {
        score += 10;
        scoreEl.textContent = score;
        generateFood();
      } else {
        snake.pop();
      }
      
      draw();
    }
  
    function gameOver() {
      stop();
      if (score > highScore) {
        highScore = score;
        localStorage.setItem('snakeHighScore', highScore.toString());
        highScoreEl.textContent = highScore;
      }
      // –ò–≥—Ä–∞ –ø—Ä–æ—Å—Ç–æ –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç—Å—è ‚Äî –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫ –ø–æ –∫–Ω–æ–ø–∫–µ
    }
  
    function start() {
      stop(); // –£–±–µ–¥–∏—Ç—å—Å—è, —á—Ç–æ —Å—Ç–∞—Ä–∞—è –∏–≥—Ä–∞ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞
      resetGame();
      isRunning = true;
      gameInterval = setInterval(update, 120);
    }
  
    function stop() {
      if (gameInterval) {
        clearInterval(gameInterval);
        gameInterval = null;
      }
      isRunning = false;
    }
  
    function handleKey(e) {
      if (!isRunning) return;
      switch (e.key) {
        case 'ArrowUp': if (direction !== 'down') nextDirection = 'up'; break;
        case 'ArrowDown': if (direction !== 'up') nextDirection = 'down'; break;
        case 'ArrowLeft': if (direction !== 'right') nextDirection = 'left'; break;
        case 'ArrowRight': if (direction !== 'left') nextDirection = 'right'; break;
      }
    }
  
    // –£–¥–∞–ª—è–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π –æ–±—Ä–∞–±–æ—Ç—á–∏–∫, –µ—Å–ª–∏ –æ–Ω –±—ã–ª
    if (window.snakeKeyHandler) {
      window.removeEventListener('keydown', window.snakeKeyHandler);
    }
    window.snakeKeyHandler = handleKey;
    window.addEventListener('keydown', handleKey);
  
    snakeGame = { start, stop };
  }
  
